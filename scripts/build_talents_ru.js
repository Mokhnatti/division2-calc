// Build teriyaki talent index (lowercase name → entry)
const tTalents = teriyaki.weaponTalents;
const tByName = {};
for (const tt of tTalents) {
  tByName[(tt.name || "").toLowerCase()] = tt;
}

// Build mikeree talent index
const mByName = {};
for (const [k, mt] of Object.entries(mikeree)) {
  mByName[(mt.name || "").toLowerCase()] = mt;
}

// Build output
const result = {};
const unmatched = [];

for (const item of VISION_RU) {
  const enName = RU_TO_EN[item.name_ru];
  if (!enName) { unmatched.push(item.name_ru + " (no mapping)"); continue; }

  const tEntry = tByName[enName.toLowerCase()];
  const mEntry = mByName[enName.toLowerCase()];

  const key = enName; // Teriyaki EN name as key
  result[key] = {
    name_ru: item.name_ru,
    name_en: enName,
    desc_ru: item.desc_ru,
    desc_en: tEntry?.description || mEntry?.description || "",
    teriyaki_id: tEntry?.id || null,
    stacks: mEntry?.stacks || null,
    bonus: mEntry?.bonus || null,
    matched_teriyaki: !!tEntry,
    matched_mikeree: !!mEntry,
  };
}

const OUT = path.join(ROOT, "data/talents_ru.json");
fs.writeFileSync(OUT, JSON.stringify(result, null, 2) + "\n", "utf8");

console.log(`Wrote data/talents_ru.json: ${Object.keys(result).length} talents`);
console.log(`Matched Teriyaki: ${Object.values(result).filter(r=>r.matched_teriyaki).length}`);
console.log(`Matched Mikeree: ${Object.values(result).filter(r=>r.matched_mikeree).length}`);
if (unmatched.length) console.log("Unmatched:", unmatched);

// Print summary table
const rows = Object.values(result).map(r => ({
  ru: r.name_ru,
  en: r.name_en,
  teriyaki: r.matched_teriyaki ? "✓" : "✗",
  mikeree: r.matched_mikeree ? "✓" : "✗",
  desc_en_len: r.desc_en.length,
}));
console.table(rows);
